/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_ADC2_Init();
  MX_ADC3_Init();
  MX_TIM1_Init();
  MX_SPI3_Init();
  /* USER CODE BEGIN 2 */
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
  HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);
  HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_2);
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_3);
  HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_3);

  HAL_ADC_Start(&hadc2);
  HAL_ADC_Start(&hadc3);

  HAL_ADCEx_MultiModeStart_DMA(&hadc1, adc_dma_buf, 15);
  int pwm_period = TIM1->ARR;

  const uint16_t encoder_reg = 0x3FFF | 0x4000 | 0x8000; 	// reg | read | parity
  const uint16_t eerfl_reg = 0x0001 | 0x4000 | 0x8000;		// reg | read | parity

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */

  // align motor to phase A
  dutyA = 0.52;
  dutyB = 0.48;
  dutyC = 0.48;

  TIM1->CCR1 = (int) (pwm_period * dutyA);
  TIM1->CCR2 = (int) (pwm_period * dutyB);
  TIM1->CCR3 = (int) (pwm_period * dutyC);

  HAL_Delay(2000);

  // calibrate encoder
  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_15, GPIO_PIN_RESET);
  HAL_Delay(0.01);
  HAL_SPI_TransmitReceive(&hspi3, (uint8_t*)&encoder_reg, (uint8_t*)&angle_mes_encoder, 1, 100);
  HAL_Delay(0.01);
  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_15, GPIO_PIN_SET);
  HAL_Delay(0.01);
  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_15, GPIO_PIN_RESET);
  HAL_Delay(0.01);
  HAL_SPI_Receive(&hspi3, (uint8_t*)&angle_mes_encoder, 1, 100);
  HAL_Delay(0.01);
  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_15, GPIO_PIN_SET);

  angle_mes_encoder &= 0x3FFF;
  angle_mes = angle_mes_encoder*ENC2RADS;	// measured angle is now in radians
  angle_not = angle_mes;	// "naught". zero position, in radians

  // clear PWM output
  TIM1->CCR1 = (int) 0;
  TIM1->CCR2 = (int) 0;
  TIM1->CCR3 = (int) 0;

  // phase angles
  angleA = angle_not;
  angleB = angle_not + PI/1.5;	// *2pi/3
  angleC = angle_not + PI/0.75;	// *4pi/3

  while (1)
  {

	// change angle with pots
	if (adc_dma_buf[13] < 4000) {	// position control mode
		adc12 = 0.3*adc_dma_buf[12] + 0.7*adc12;
		angle_tgt = 0.5 * (2*PI*((float) adc12) / 4095.0);
	}
	else {							// bonus speed control mode!
		angle_tgt += 0.2 * ((float) adc_dma_buf[12]) / 4095.0;
	}

	// retrieve rotor position
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_15, GPIO_PIN_RESET);
	HAL_SPI_TransmitReceive(&hspi3, (uint8_t*)&encoder_reg, (uint8_t*)&angle_mes_encoder, 1, 100);
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_15, GPIO_PIN_SET);
	angle_mes = (angle_mes_encoder & 0x3FFF) * ENC2RADS;

	// ******control position******
	// error
	angle_err = angle_tgt - angle_mes;	// rads
	while(angle_err >  PI) angle_err -= 2*PI;
	while(angle_err < -PI) angle_err += 2*PI;

	// amplitude of voltage vector
	ampl = fabs(gain_p*angle_err);
	if (ampl>1) ampl = 1;

	// angle of voltage vector - set 90deg
	angle_elec = POLE_PAIRS*angle_mes + angle_not;
	angleV = angle_elec + PI/2.0*(angle_err>THRESH_CTRL) - PI/2.0*(angle_err<-THRESH_CTRL);	// orthogonal torque vector

	// phase vector magnitudes
	magA = ampl * cosf(angleV-angleA);
	magB = ampl * cosf(angleV-angleB);
	magC = ampl * cosf(angleV-angleC);

	// assign duty cycles
	dutyA = 0.5 + magA*0.5;
	dutyB = 0.5 + magB*0.5;
	dutyC = 0.5 + magC*0.5;
	// **************************

	// assign duty cycles
    TIM1->CCR1 = (int) (pwm_period * dutyA);
    TIM1->CCR2 = (int) (pwm_period * dutyB);
    TIM1->CCR3 = (int) (pwm_period * dutyC);

    HAL_Delay(0.01);
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

